<h4>1. How do I check the status of a transaction?</h4>
<p class="lead">After you perform a transaction with the Cryptany mobile app, you will receive a
confirmation email to the email address you provided in the start a new transaction form in
the Cryptany mobile wallet. This email will contain a web link to check the status of your
transaction.</p>

<h4>2. Can I transfer more than 1 USD?</h4>
<p class="lead">At this point in the development of the Cryptany mobile app, the maximum transaction
amount is limited to 1 USD. Very soon you will be able to transfer larger volumes of a
variety of currencies.</p>

<h4>3. When will I get the fiat currency on my card?</h4>
<p class="lead">The Cryptany system operates between the sphere of decentralized crypto currencies and
the traditional banking system and is limited by the mechanisms and speed of the later.
The transfer of fiat currency to your card may take anywhere from a few moments to up to
3 days, depending on the banking system involved. The usual time for a transfer is a little
less than 2 hours, however.</p>
<p>If you do not receive your fiat after 3 days, please contact us.</p>

<h4>4. Why I can only send Ethereum?</h4>
<p class="lead">Currently the only cryptocurrency available for transfer on the Cryptany mobile app is
Ethereum. More cryptocurrencies will be available in a future version of the Cryptany
mobile wallet.</p>

<h4>5. Can I reverse a transaction made with my Cryptany Wallet?</h4>
<p class="lead">
Unfortunately, transactions made with the Cryptany Wallet are not reversible. An Ethereum
transaction already broadcast to the network is permanent and irreversible. This is an
important feature of the Ethereum protocol which prevents payment fraud.
</p>

<h4>6. Does Cryptany conduct KYC / AML checks?</h4>
<p class="lead">
Every transaction on the Cryptany mobile app must pass through KYC / AML checks. You
may be required to provide additional document to complete a transaction. If for some
reason a transaction is blocked by the KYC / AML check, your fiat or crypto transfer will be
fully refunded to your wallet.
</p>

<h4>7. What is the maximum transaction amount?</h4>
<p class="lead">
In beta version of the Cryptany mobile app, transactions are limited to the equivalent of 1
USD. Larger transactions will be blocked by the system and you will be fully refunded.</p>
<p class="lead">
Please remember that in order to get a refund, you should not use accounts on
exchanges.
</p>
